# 2-7. Stripping Names

name = " oziel velazquez "

print(f"\n The original name, with whitespaces: '{name}'.")
print(f"\n The name with no whitespaces on the left: '{name.lstrip()}'.")
print(f"\n The name with no whitespaces on the right: '{name.rstrip()}'.")
print(f"\n The name with no whitespaces: '{name.strip()}'.\n")